// for loop

var cars = ['BMW', 'Volvo', 'Saab', 'Ford'];
var i = 0;
var text = '';

for (;cars[i];) {
  text += cars[i] + '<br>';
  i++;
  console.log(cars);
}

// while loop

var cars = ['BMW', 'Volvo', 'Saab', 'Ford'];
var i = 0;
var text = '';

while (cars[i]) {
  text += cars[i] + '<br>';
  i++;
  console.log(cars);
}